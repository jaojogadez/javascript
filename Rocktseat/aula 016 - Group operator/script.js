let number = 10
console.log(number)

number++ // - isso é um incremento
console.log(number)


console.log(number++) // - incrementa após
console.log(number)

console.log(++number) // - incrementa antes

// Decremento

console.log("Decremento após: ", number--)
console.log(number)
console.log("Decremento antes: ", --number)
console.log(number)


// Incrementar mais de um (adicionar)
number += 10 // - isso pega o contéudo e adicona a condição
console.log(number)


// Decrementar mais de um (remover)
number -= 5
console.log(number)