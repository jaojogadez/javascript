let one = 1 
let two = 2

// igual a ==
console.log("IGAUAL A")
console.log(one == two)
console.log(one == 1)
console.log(one == "1")

// diferente de !=

console.log("DIFERENTE DE")
console.log(one != two)


// estritamente igual a === (tipo e valor)
console.log("ESTRITAMENTE IGUAL A")
console.log(one === 1)
console.log(one === "1")

// estritamente diferente de !== (tipo e valor)
console.log("ESTRITAMENTE DIFERENTE DE")
console.log(one !== two)
console.log(one !== 1)
console.log(one !== 2)
console.log(one !== "2")
console.log(one !== "1")

